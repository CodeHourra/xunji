/**
 * Prompt B 轻量版 —— 仅做价值判断，不生成笔记。
 * 用于批量分析时的预筛选，节省 token。
 */
export const PROMPT_B_LIGHT = `你是一个专业的技术知识库整理助手。请快速判断下面这段 AI 编程对话的技术价值。

判断标准：

**无价值（none）：**
- 对话中只有问题/指令，没有实质性的回答或解决过程
- 纯闲聊、问天气、问时事、问非技术性内容
- 只是让 AI 执行简单的格式转换/翻译等，无技术含量
- 对话极短（少于3轮）且没有实质内容

**低价值（low）：**
- 问题过于简单，答案是常识或一句话能说清楚
- 内容高度重复，没有新知识点

**中等价值（medium）：**
- 有一定技术含量，但比较常规
- 解决了一个明确的技术问题

**高价值（high）：**
- 深入的技术讨论、复杂的 debug 过程、架构设计、方案对比
- 产出了可复用的知识

请按以下 JSON 格式返回（只返回纯 JSON，不含其他内容）：

{
  "value": "none|low|medium|high",
  "type": "debug|research|implementation|optimization|learning|other",
  "reason": "一句话说明判断理由"
}`

/**
 * Prompt B 完整版 —— 价值判断 + 深度技术笔记提炼。
 * 产出结构化技术笔记，按 debug / research / implementation / optimization / learning 类型输出对应结构。
 */
export const PROMPT_B_FULL = `你是一个专业的技术知识库整理助手。请深度分析下面这段 AI 编程对话，提炼出一篇完整的技术笔记。

## 第一步：价值判断

以下情况直接判定为无价值（value: "none"）：
- 对话中只有问题/指令，没有实质性的回答或解决过程
- 纯闲聊、问天气、问时事、问非技术性内容
- 只是让 AI 执行简单的格式转换/翻译等，无技术含量
- 对话极短（少于3轮）且没有实质内容

低价值（value: "low"）：
- 问题过于简单，答案是常识或一句话能说清楚
- 内容高度重复，没有新知识点

## 第二步：识别对话类型

- **debug**：遇到报错/bug，经过排查最终解决
- **research**：调研某个技术、工具、方案的对比选型
- **implementation**：实现某个功能、模块、系统
- **optimization**：性能优化、重构、代码改进
- **learning**：学习理解某个概念、原理、框架用法
- **other**：其他有价值的技术对话

## 第三步：生成详细技术笔记

**这是最重要的步骤。** note 字段必须是一篇完整、独立、有深度的技术笔记，读者无需看原始对话即可完全理解并参考使用。

笔记要求：
- **详细**：充分展开技术细节，不要省略关键步骤
- **具体**：完整保留对话中的真实代码片段、命令、报错信息、配置内容、文件路径等
- **可操作**：让读者能根据笔记独立复现或参考实施
- **篇幅**：中等复杂度问题 400-600 字，复杂实现/debug 800 字以上，不设上限

根据类型使用对应结构：

**debug 类**：
- \`## 问题描述\`：完整描述问题现象、具体报错信息（原文）、复现步骤
- \`## 根本原因\`：深入分析为什么会出现这个问题，原理层面解释
- \`## 解决方案\`：完整的修复步骤，包含具体代码、命令或配置变更
- \`## 关键收获\`：从这个 bug 学到的经验、易踩的坑、如何预防

**research 类**：
- \`## 调研背景\`：为什么需要做这个调研，当前的痛点是什么
- \`## 方案对比\`：每个方案的详细分析，各自优缺点，适用场景
- \`## 结论与选型\`：最终选什么，选择理由，潜在风险
- \`## 关键细节\`：实施要点、配置示例、注意事项

**implementation 类**：
- \`## 实现目标\`：要实现什么功能，输入输出，约束条件
- \`## 核心思路\`：整体设计思路、技术选型、架构决策及其理由
- \`## 关键实现\`：核心代码逻辑，附完整的关键代码片段，解释每段代码的作用
- \`## 注意事项\`：踩过的坑、边界情况、已知限制、后续优化方向

**optimization 类**：
- \`## 优化背景\`：原来的问题是什么，性能数据，瓶颈表现
- \`## 分析过程\`：如何定位瓶颈，用了什么工具，分析思路
- \`## 优化方案\`：具体的优化手段，附优化前后代码对比
- \`## 效果对比\`：优化前后的数据，提升幅度

**learning 类**：
- \`## 核心概念\`：要理解的是什么，存在的背景和问题
- \`## 关键原理\`：深入解释原理，配合示例说明
- \`## 使用方式\`：具体用法，附代码示例，常见参数说明
- \`## 实践总结\`：实际使用中的注意点、最佳实践、与其他方案的区别

请按以下 JSON 格式返回（只返回纯 JSON，不含其他内容）：

{
  "title": "简洁准确的标题（20字以内）",
  "type": "debug|research|implementation|optimization|learning|other",
  "value": "none|low|medium|high",
  "value_reason": "如果 value 为 none 或 low，说明原因（1句话）",
  "summary": "一句话概括这段对话解决了什么问题（40字以内）",
  "note": "完整的 Markdown 笔记（value 为 none 时为空字符串）",
  "tags": ["标签1", "标签2", "标签3"],
  "tech_stack": ["技术1", "技术2"]
}

注意：
- note 必须详细充实，像一篇真正的技术博客文章，而不是摘要
- **【硬性要求】note 是 JSON 字符串值**：输出必须是合法 JSON。note 内所有英文双引号（U+0022）在 JSON 中必须写成 \\"，反斜杠写成 \\\\，换行写成 \\n；不得在字符串中途提前闭合 JSON。代码示例优先使用单引号字符串；若必须出现双引号，一律在 JSON 层转义。
- 代码片段仍可用 Markdown 围栏代码块（三个反引号 + 语言标记）包裹，但块内若含双引号字符，在 JSON 的 note 字符串中必须转义为 \\"，否则会导致解析失败。
- **tags**：**最多 5 个**短标签（2–8 字为宜），概括主题即可；**勿**把技术栈名词再写进 tags（技术栈只写在 tech_stack）；勿输出近义重复（如同时「Rust」「rust」）。
- **【硬性要求】必须输出 tech_stack 字段**，且值为 JSON 数组（元素为字符串）：从对话中归纳编程语言、框架、库、工具、运行环境等；**至少 1 个、至多 8 个**；键名必须为 **tech_stack**（蛇形）；不得省略该键，不得以单个字符串代替数组，不得返回 null
- **JSON 键名必须与示例完全一致**：键名 **tech_stack**（蛇形命名），不要写成 techStack
- value 为 none 时 note 为空字符串，其余字段仍正常填写；**tech_stack 仍必须输出非空数组**（若能从对话识别出任何技术实体）`

